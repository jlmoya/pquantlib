# C++ ``c[2] = {-1, 1}`` sign table.
_C = (-1, 1)


class ZigguratRng:
    """Standard-normal RNG via the Ziggurat method.

    # C++ parity: ``class ZigguratRng`` in
    # ql/experimental/math/zigguratrng.hpp:52-60 (v1.42.1).

    The MT19937 uniform source is bit-identical to C++; combined with
    the verbatim ziggurat tables and the same 24-bit accept/reject
    layout, the standard-normal stream matches C++ bit-for-bit. A
    nonzero seed is required (the C++ default-0 ``SeedGenerator`` clock
    fallback is not ported — see ``MersenneTwisterUniformRng``).
    """

    __slots__ = ("_mt32",)

    def __init__(self, seed: int) -> None:
        # C++ default seed is 0 -> SeedGenerator; the ported MT refuses
        # seed 0 explicitly (clock fallback not ported).
        self._mt32: MersenneTwisterUniformRng = MersenneTwisterUniformRng(seed)

    def _next_gaussian(self) -> float:
        # C++ parity: ``ZigguratRng::nextGaussian`` zigguratrng.cpp:241-271.
        while True:
            j = self._mt32.next_int32()  # 32 bits of randomness
            f = j & 1  # 1 bit to choose a tail sign
            j >>= 1
            i = j & 0x7F  # 7 bits to choose a strip
            j >>= 7  # the last 24 bits for acceptance/rejection
            # x is uniform within the i-th strip.
            x = (_C[f] * j) * _W[i]
            if j < _K[i]:  # accept x
                return x

            # handle rejections
            if i != 0:  # upper strips
                if (_F[i - 1] - _F[i]) * self._mt32.next_real() + _F[i] < math.exp(
                    -0.5 * x * x
                ):
                    return x
            else:  # base strip, sample from the tail
                return _C[f] * InverseCumulativeNormal.standard_value(
                    _P * self._mt32.next_real() + _Q
                )

    def next(self) -> Sample:
        """Return one standard-normal sample with weight 1.0.

        # C++ parity: ``ZigguratRng::next`` zigguratrng.hpp:55.
        """
        return Sample(value=self._next_gaussian(), weight=1.0)

    def dimension(self) -> int:
        """Scalar RNG — dimension is always 1."""
        return 1
