"""Ziggurat-algorithm standard-normal random-number generator.

# C++ parity: ql/experimental/math/zigguratrng.{hpp,cpp} (v1.42.1)
# (Copyright 2010 Kakhkhor Abdijalilov).

Returns standard normal variates via the Ziggurat method of Marsaglia
and Tsang (2000), "The Ziggurat Method for Generating Random
Variables", *Journal of Statistical Software* 5 (8). The underlying
uniform source is the 32-bit MT19937 (``MersenneTwisterUniformRng``).

The C++ implementation deliberately avoids the step-1/step-2
correlation of the original paper by consuming only the leftmost 24
bits of each 32-bit MT word (1 bit for the tail sign, 7 bits for the
strip index, 24 bits for accept/reject). The Python port reproduces
this bit-layout exactly so that — because the MT uniform stream and the
``InverseCumulativeNormal`` tail quantile are already bit-identical to
C++ — the emitted standard-normal stream is bit-identical to the C++
``ZigguratRng`` for any nonzero seed.

The tabulated constants ``_F`` (strip exp values), ``_K`` (acceptance
thresholds ``2^24 x[i]/x[i+1]``) and ``_W`` (``2^-24 x[i]``) are
transcribed verbatim from ``zigguratrng.cpp``.
"""

