def _five_year_vanilla_swap(
    fixed_rate: float, swap_type: SwapType = SwapType.Payer
) -> tuple[VanillaSwap, YieldTermStructureProtocol, Date]:
    """Build the 5y vanilla swap used by the C++ probe.

    Settings: eval_date = 17 Jan 2024 (Wed), 1M EUR notional, semi-annual
    fixed leg vs quarterly Euribor3M, FlatForward(5%) curve.
    """
    eval_date = Date.from_ymd(17, Month.January, 2024)
    curve = cast(
        YieldTermStructureProtocol,
        FlatForward.from_rate(
            eval_date, 0.05, Actual360(), Compounding.Continuous, Frequency.Annual
        ),
    )
    idx = Euribor(Period(3, TimeUnit.Months), curve)

    cal = TARGET()
    settle = cal.advance(eval_date, 2, TimeUnit.Days)
    end = settle + Period(5, TimeUnit.Years)

    fixed_sched = Schedule.from_rule(
        settle, end, Period(6, TimeUnit.Months), cal,
        BusinessDayConvention.ModifiedFollowing,
        BusinessDayConvention.ModifiedFollowing,
        DateGeneration.Backward, False,
    )
    float_sched = Schedule.from_rule(
        settle, end, Period(3, TimeUnit.Months), cal,
        BusinessDayConvention.ModifiedFollowing,
        BusinessDayConvention.ModifiedFollowing,
        DateGeneration.Backward, False,
    )
    swap = VanillaSwap(
        swap_type, 1_000_000.0,
        fixed_sched, fixed_rate, Thirty360(Convention.BondBasis),
        float_sched, idx, 0.0, idx.day_counter(),
    )
    swap.set_pricing_engine(DiscountingSwapEngine(curve))
    return swap, curve, settle


# --- Schedule sanity -----------------------------------------------------


def test_vanilla_swap_leg_sizes(cluster_refs: dict[str, dict[str, float]]) -> None:
    """Per probe: 5y semi-annual = 10 fixed periods; quarterly = 20 float periods."""
    swap, _, _ = _five_year_vanilla_swap(0.05)
    expected = cluster_refs["vanilla_swap_5y"]
    assert len(swap.fixed_leg()) == expected["fixed_leg_size"]
    assert len(swap.floating_leg()) == expected["floating_leg_size"]


