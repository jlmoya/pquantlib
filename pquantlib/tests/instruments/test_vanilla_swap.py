def test_vanilla_swap_npv_matches_probe(cluster_refs: dict[str, dict[str, float]]) -> None:
    swap, _, _ = _five_year_vanilla_swap(0.05)
    expected = cluster_refs["vanilla_swap_5y"]
    # LOOSE tier: NPV chain depends on Schedule + leg-iteration + IBOR forecast;
    # accumulates lots of floating-point work where any small idiom drift
    # could push past TIGHT. 1e-8 abs/rel covers genuine equivalence.
    tolerance.loose(swap.npv(), expected["npv"])


