# --- Receiver variant ----------------------------------------------------


def test_vanilla_swap_receiver_flips_npv_sign(cluster_refs: dict[str, dict[str, float]]) -> None:
    swap, _, _ = _five_year_vanilla_swap(0.06, swap_type=SwapType.Receiver)
    expected = cluster_refs["vanilla_swap_receiver_6pct"]
    tolerance.loose(swap.npv(), expected["npv"])
    # The fair_rate stays roughly 5.14% — receiver/payer don't change it
    # since both swaps look at the same fixed/float break-even.
    tolerance.loose(swap.fair_rate(), expected["fair_rate"])


# --- Direct ctor inspectors ----------------------------------------------


def test_vanilla_swap_inspectors() -> None:
    swap, curve, settle = _five_year_vanilla_swap(0.05)
    assert swap.swap_type() == SwapType.Payer
    assert swap.nominal() == 1_000_000.0
    assert swap.fixed_rate() == 0.05
    assert swap.spread() == 0.0
    assert swap.payer(0)  # fixed leg paid
    assert not swap.payer(1)  # floating leg received
    # Schedule end ≈ settle + 5y; payment-cal advances make it exact.
    assert swap.maturity_date() > settle
    _ = curve  # silence unused-var
